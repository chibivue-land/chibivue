function esc(value) {
  return String(value)
    .replaceAll("&", "&amp;")
    .replaceAll("<", "&lt;")
    .replaceAll(">", "&gt;")
    .replaceAll('"', "&quot;");
}

function attrs(values) {
  return Object.entries(values)
    .filter(([, value]) => value !== undefined && value !== null && value !== false)
    .map(([key, value]) => `${key}="${esc(value)}"`)
    .join(" ");
}

function text(x, y, value, options = {}) {
  const { className = "label", anchor = "middle", size, color, weight, lineHeight = 20 } = options;
  const lines = String(value).split("\n");
  const baseAttrs = attrs({
    x,
    y,
    "text-anchor": anchor,
    class: className,
    "font-size": size,
    fill: color,
    "font-weight": weight,
  });
  if (lines.length === 1) return `<text ${baseAttrs}>${esc(value)}</text>`;
  return `<text ${baseAttrs}>${lines
    .map((line, index) => {
      const dy = index === 0 ? 0 : lineHeight;
      return `<tspan x="${x}" dy="${dy}">${esc(line)}</tspan>`;
    })
    .join("")}</text>`;
}

function box(x, y, width, height, label, options = {}) {
  const {
    className = "box",
    titleClass = "box-label",
    sub,
    accent = false,
    muted = false,
    dashed = false,
  } = options;
  const classes = [className, accent && "accent", muted && "muted", dashed && "dashed"]
    .filter(Boolean)
    .join(" ");
  const labelY = y + height / 2 + (sub ? -6 : 6);
  return `<g>
  <rect ${attrs({ x, y, width, height, rx: 8, ry: 8, class: classes })}/>
  ${text(x + width / 2, labelY, label, { className: titleClass })}
  ${sub ? text(x + width / 2, y + height / 2 + 20, sub, { className: "sub-label", lineHeight: 16 }) : ""}
</g>`;
}

function lane(x, y, width, height, label) {
  return `<g>
  <rect ${attrs({ x, y, width, height, rx: 8, ry: 8, class: "lane" })}/>
  ${text(x + 24, y + 34, label, { className: "lane-label", anchor: "start" })}
</g>`;
}

function arrow(x1, y1, x2, y2, options = {}) {
  const { label, dashed = false, color = brand.mint, bend } = options;
  const classes = ["arrow", dashed && "dashed"].filter(Boolean).join(" ");
  const shape = bend
    ? `<path ${attrs({ d: `M ${x1} ${y1} C ${bend[0]} ${bend[1]}, ${bend[2]} ${bend[3]}, ${x2} ${y2}`, class: classes, stroke: color })}/>`
    : `<line ${attrs({ x1, y1, x2, y2, class: classes, stroke: color })}/>`;
  const labelSvg = label
    ? text((x1 + x2) / 2, (y1 + y2) / 2 - 10, label, { className: "edge-label" })
    : "";
  return `${shape}${labelSvg}`;
}

function pill(x, y, width, height, label, options = {}) {
  return `<g>
  <rect ${attrs({ x, y, width, height, rx: height / 2, ry: height / 2, class: options.accent ? "pill accent" : "pill" })}/>
  ${text(x + width / 2, y + height / 2 + 6, label, { className: "pill-label" })}
</g>`;
}

function codeChip(x, y, label, width = 180) {
  return `<g>
  <rect ${attrs({ x, y, width, height: 36, rx: 6, ry: 6, class: "code-chip" })}/>
  ${text(x + width / 2, y + 23, label, { className: "code-label" })}
</g>`;
}

