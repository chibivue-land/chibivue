function keyedChildrenDiagram({ title, subtitle, oldItems, newItems, links, notes = [] }) {
  const maxItems = Math.max(oldItems.length, newItems.length);
  const notesY = 126 + maxItems * 58 + 34;
  const height = notes.length > 0 ? notesY + notes.length * 22 + 38 : 450;
  const row = (x, y, label, items, highlightIndex) => {
    const cells = items
      .map((item, index) => {
        const cls = index === highlightIndex ? "box accent" : "box";
        return `<rect ${attrs({ x, y: y + index * 58, width: 120, height: 42, rx: 8, ry: 8, class: cls })}/>
${text(x + 60, y + index * 58 + 27, item, { className: "box-label" })}`;
      })
      .join("\n");
    return `${text(x + 60, y - 18, label, { className: "lane-label" })}${cells}`;
  };
  const arrows = links
    .map(([from, to, label, dashed]) =>
      arrow(258, 150 + from * 58, 452, 150 + to * 58, {
        label,
        dashed,
        color: dashed ? brand.orange : brand.mint,
        bend: [320, 150 + from * 58, 390, 150 + to * 58],
      }),
    )
    .join("\n");
  return diagram({
    width: 720,
    height,
    title,
    subtitle,
    body: `
${row(138, 126, "c1: old children", oldItems)}
${row(462, 126, "c2: new children", newItems)}
${arrows}
${notes
  .map((note, index) =>
    text(360, notesY + index * 22, note, { className: index === 0 ? "warn" : "muted-label" }),
  )
  .join("\n")}
`,
  });
}

